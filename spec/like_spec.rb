require 'rails_helper'

RSpec.describe Like, type: :model do
  let(:user) { User.create(name: 'John Doe', posts_counter: 0) }
  let(:post) { Post.create(author_id: user, title: 'Sample Post', comments_counter: 0, likes_counter: 0) }
  let(:like) { Like.new(post_id: post, user:) }

  describe '#update_post_likes_counter' do
    it 'updates the post likes_counter attribute' do
      like.save
      expect(post.likes_counter).to eq(post.likes.count)
    end
  end
end
